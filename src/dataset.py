import os
import glob
import numpy as np

try:
    import torch
    HAS_TORCH = True
except ImportError:
    HAS_TORCH = False
    torch = None

class TIRDataset:

    """
    Dataset class for loading coregistered thermal infrared (TIR) and RGB patches.
    Handles loading from .npy files generated by the baseline patch creation script.
    """
    def __init__(self, patches_dir, transform=None, normalize=True):
        """
        Args:
            patches_dir (str): Path to directory containing product patch samples 
                               (e.g., output/patches/ or output/patches/<product_id>/).
            transform (callable, optional): Optional transform to be applied on samples.
            normalize (bool): Whether to normalize image arrays into standard [0, 1] or [-1, 1] ranges.
        """
        self.patches_dir = patches_dir
        self.transform = transform
        self.normalize = normalize
        self.samples = []

        # Search for sample directories
        # Can be output/patches/<product_id>/sample_XXX or output/patches/sample_XXX
        search_pattern = os.path.join(patches_dir, '**', 'sample_*')
        sample_dirs = glob.glob(search_pattern, recursive=True)
        sample_dirs = [d for d in sample_dirs if os.path.isdir(d)]
        
        for s_dir in sorted(sample_dirs):
            tir_200_path = os.path.join(s_dir, 'tir_200m.npy')
            tir_100_path = os.path.join(s_dir, 'tir_100m_512.npy')
            rgb_100_path = os.path.join(s_dir, 'rgb_100m_512.npy')

            if os.path.exists(tir_200_path) and os.path.exists(tir_100_path) and os.path.exists(rgb_100_path):
                self.samples.append({
                    'tir_200m': tir_200_path,
                    'tir_100m': tir_100_path,
                    'rgb_100m': rgb_100_path
                })

    def __len__(self):
        return len(self.samples)

    def _normalize_array(self, arr, is_rgb=False):
        """Dynamic percent/min-max normalization to handle satellite radiometric depth."""
        arr = arr.astype(np.float32)
        if is_rgb:
            # Multi-spectral RGB reflectance usually range 0-65535 or 0-10000 depending on USGS scale
            # Perform percentile clipping for stability
            p_low, p_high = np.percentile(arr, (1, 99))
            if p_high > p_low:
                arr = np.clip((arr - p_low) / (p_high - p_low), 0.0, 1.0)
            else:
                arr = np.clip(arr / 65535.0, 0.0, 1.0)
        else:
            # Thermal IR brightness values
            p_low, p_high = np.percentile(arr, (1, 99))
            if p_high > p_low:
                arr = np.clip((arr - p_low) / (p_high - p_low), 0.0, 1.0)
            else:
                max_val = arr.max()
                if max_val > 0:
                    arr = arr / max_val
        return arr

    def __getitem__(self, idx):
        sample_paths = self.samples[idx]

        tir_200 = np.load(sample_paths['tir_200m'])
        tir_100 = np.load(sample_paths['tir_100m'])
        rgb_100 = np.load(sample_paths['rgb_100m'])

        # Ensure channel dimensions: (C, H, W)
        if tir_200.ndim == 2:
            tir_200 = tir_200[np.newaxis, ...]
        if tir_100.ndim == 2:
            tir_100 = tir_100[np.newaxis, ...]
        if rgb_100.ndim == 2:
            rgb_100 = np.stack([rgb_100]*3, axis=0)

        if self.normalize:
            tir_200 = self._normalize_array(tir_200, is_rgb=False)
            tir_100 = self._normalize_array(tir_100, is_rgb=False)
            rgb_100 = self._normalize_array(rgb_100, is_rgb=True)

        if HAS_TORCH:
            t_tir_200 = torch.from_numpy(tir_200).float()
            t_tir_100 = torch.from_numpy(tir_100).float()
            t_rgb_100 = torch.from_numpy(rgb_100).float()

            if self.transform:
                if np.random.rand() > 0.5:
                    t_tir_200 = torch.flip(t_tir_200, dims=[-1])
                    t_tir_100 = torch.flip(t_tir_100, dims=[-1])
                    t_rgb_100 = torch.flip(t_rgb_100, dims=[-1])
                if np.random.rand() > 0.5:
                    t_tir_200 = torch.flip(t_tir_200, dims=[-2])
                    t_tir_100 = torch.flip(t_tir_100, dims=[-2])
                    t_rgb_100 = torch.flip(t_rgb_100, dims=[-2])

            return {
                'tir_200m': t_tir_200,
                'tir_100m': t_tir_100,
                'rgb_100m': t_rgb_100
            }
        else:
            if self.transform:
                if np.random.rand() > 0.5:
                    tir_200 = np.flip(tir_200, axis=-1)
                    tir_100 = np.flip(tir_100, axis=-1)
                    rgb_100 = np.flip(rgb_100, axis=-1)
                if np.random.rand() > 0.5:
                    tir_200 = np.flip(tir_200, axis=-2)
                    tir_100 = np.flip(tir_100, axis=-2)
                    rgb_100 = np.flip(rgb_100, axis=-2)

            return {
                'tir_200m': tir_200,
                'tir_100m': tir_100,
                'rgb_100m': rgb_100
            }

